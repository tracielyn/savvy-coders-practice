const myName = "Tracie Walka";

//Template Literal
console.log(`Hello World! This is ${myName}`);

//Object Literal
const me = {
  name: "Tracie",
  age: 33,
  isBeingTruthful: false,
  hairColor: "ombre",
  eyeColor: "grey"
};

//We are using BRACKET NOTATION
console.log(me["name"]);

//We are using DOT NOTATION
console.log(me.age);

